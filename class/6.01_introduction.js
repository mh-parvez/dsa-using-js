
`
Object-oriented programming (OOP) is a computer programming model that organizes software design around data, or objects, rather than functions and logic.

> Four Pillars

01.Encapsulation: This principle states that all important information is contained inside an object and only select information is exposed. 

02.Abstraction: Objects only reveal internal mechanisms that are relevant for the use of other objects, hiding any unnecessary implementation code

03.Inheritance: Classes can reuse code from other classes. Relationships and subclasses between objects can be assigned, enabling developers to reuse common logic while still maintaining a unique hierarchy.

04.Polymorphism: Objects are designed to share behaviors and they can take on more than one form. The program will determine which meaning or usage is necessary for each execution of that object from a parent class, reducing the need to duplicate code.
`

